Wrapcol = function ()
	local text
	local signs = '%s'
	local folds = '%C'
	text = signs .. folds .. string.format('%3d ', vim.v.lnum)
	local vnum = vim.v.virtnum
	if vnum > 0 then
		text = text .. '+' .. string.format('%3d ', vim.v.virtnum)
	end
	return text
end
